def clean_name_for_email(name):
    """تنظيف الاسم لاستخدامه في الإيميل"""
    # إزالة التشكيل والأحرف الخاصة
    name = name.replace('أ', 'a').replace('إ', 'e').replace('آ', 'a')
    name = name.replace('ة', 'h').replace('ى', 'a').replace('ؤ', 'o')
    name = name.replace('ئ', 'y').replace('ء', '').replace(' ', '.')
    # إزالة الأحرف غير الإنجليزية والأرقام والنقاط
    cleaned = ''.join(c for c in name if c.isascii() or c == '.')
    return cleaned.lower().strip('.')


def generate_email(first_name, last_name, domain=None):
    """توليد إيميل بناءً على الاسم"""
    first = clean_name_for_email(first_name)
    last = clean_name_for_email(last_name)
    
    # التأكد من أن الأسماء ليست فارغة
    if not first:
        first = f"user{random.randint(1, 9999)}"
    if not last:
        last = f"egy{random.randint(1, 9999)}"
    
    if not domain:
        domain = random.choice(EMAIL_DOMAINS)
    
    patterns = [
        f"{first}.{last}@{domain}",
        f"{first}{last}@{domain}",
        f"{first}.{last}{random.randint(1, 99)}@{domain}",
        f"{first}{random.randint(1, 999)}@{domain}",
        f"{last}.{first}@{domain}",
        f"{first}_{last}@{domain}",
    ]
    
    # إضافة نمط مع الحرف الأول فقط إذا كان الاسم غير فارغ
    if len(first) > 0:
        patterns.append(f"{first[0]}.{last}@{domain}")
    
    return random.choice(patterns)


def random_date_between(start_date, end_date):
    """توليد تاريخ عشوائي بين تاريخين"""
    days_between = (end_date - start_date).days
    random_days = random.randint(0, days_between)
    random_hours = random.randint(0, 23)
    random_minutes = random.randint(0, 59)
    random_seconds = random.randint(0, 59)
    
    random_date = start_date + timedelta(days=random_days, 
                                          hours=random_hours, 
                                          minutes=random_minutes, 
                                          seconds=random_seconds)
    return random_date


def create_egyptian_participants(total_count=1495):
    """إنشاء 1495 مشارك مصري حقيقي"""
    
    # تحديد فترة التسجيل: من 15/3/2026 حتى اليوم (7/4/2026)
    start_date = datetime(2026, 3, 15)  # 15 مارس 2026
    end_date = datetime(2026, 4, 7)     # 7 أبريل 2026 (اليوم)
    
    print(f"بدء إنشاء {total_count} مشارك مصري حقيقي...")
    print(f"فترة التسجيل: من 15/03/2026 حتى 07/04/2026")
    print("=" * 60)
    
    created_count = 0
    used_emails = set()
    
    # توزيع المشاركين على الجامعات
    universities_count = len(EGYPTIAN_UNIVERSITIES)
    participants_per_university = total_count // universities_count
    remainder = total_count % universities_count
    
    for uni_idx, university in enumerate(EGYPTIAN_UNIVERSITIES):
        # عدد المشاركين لهذه الجامعة
        count_for_this_uni = participants_per_university + (1 if uni_idx < remainder else 0)
        
        for _ in range(count_for_this_uni):
            # اختيار النوع (توزيع متوازن تقريباً)
            gender = random.choice(['male', 'female'])
            
            if gender == 'male':
                first_name = random.choice(MALE_NAMES)
            else:
                first_name = random.choice(FEMALE_NAMES)
            
            last_name = random.choice(FAMILY_NAMES)
            full_name = f"{first_name} {last_name}"
            
            # توليد إيميل فريد
            max_attempts = 10
            email = None
            for _ in range(max_attempts):
                proposed_email = generate_email(first_name, last_name)
                if proposed_email not in used_emails:
                    email = proposed_email
                    used_emails.add(email)
                    break
            
            if not email:
                # إذا فشلنا في توليد إيميل فريد، أضف رقم عشوائي
                email = f"{clean_name_for_email(first_name)}.{clean_name_for_email(last_name)}{random.randint(1000, 9999)}@gmail.com"
                used_emails.add(email)
            
            # إنشاء المشارك مع تاريخ عشوائي في الفترة المطلوبة
            try:
                random_registration_date = random_date_between(start_date, end_date)
                
                participant = Participant.objects.create(
                    name=full_name,
                    email=email,
                    university=university,
                    gender=gender,
                    age=random.randint(18, 26),
                    college=random.choice(COLLEGES),
                    year=random.choice(['1', '2', '3', '4']),
                    primary_website=random.choice(['sports', 'entertainment', 'educational', 'political', 'other']),
                    computer_skill=random.choice(['beginner', 'intermediate', 'advanced']),
                    daily_hours=random.choice(['less_than_1', '1_to_2', '3_to_4', '5_to_6', 'more']),
                )
                
                # تعديل التاريخ بعد الإنشاء
                participant.created_at = random_registration_date
                participant.save(update_fields=['created_at'])
                
                created_count += 1
                
                if created_count % 100 == 0:
                    print(f"✓ تم إنشاء {created_count} مشارك... ({created_count/total_count*100:.1f}%)")
                    
            except Exception as e:
                print(f"خطأ في إنشاء المشارك: {e}")
                continue
    
    print(f"\n✓ تم إنشاء {created_count} مشارك بنجاح!")
    return created_count


def create_realistic_answers():
    """إنشاء إجابات واقعية (توزيع طبيعي)"""
    participants = Participant.objects.all()
    questions = Question.objects.filter(is_active=True)
    
    if not participants or not questions:
        print("لا يوجد مشاركين أو أسئلة لإنشاء الإجابات")
        return
    
    # توزيع واقعي للإجابات
    # نعم = 35-45%، إلى حد ما = 30-40%، لا = 20-30%
    answer_choices = ['yes', 'somewhat', 'no']
    weights = [0.40, 0.35, 0.25]  # 40% نعم، 35% إلى حد ما، 25% لا
    
    total_answers = len(participants) * len(questions)
    print(f"\nبدء إنشاء {total_answers:,} إجابة...")
    print("=" * 60)
    
    created_count = 0
    for participant in participants:
        # تاريخ الإجابة يكون بعد تاريخ تسجيل المشارك بفترة قصيرة (دقائق إلى ساعات)
        participant_date = participant.created_at
        answer_date = participant_date + timedelta(minutes=random.randint(5, 180))  # بعد 5 دقائق إلى 3 ساعات
        
        for question in questions:
            answer = random.choices(answer_choices, weights=weights)[0]
            
            ans_obj, _ = Answer.objects.get_or_create(
                participant=participant,
                question=question,
                defaults={'answer': answer}
            )
            # تعديل تاريخ الإجابة
            ans_obj.created_at = answer_date
            ans_obj.save(update_fields=['created_at'])
            created_count += 1
        
        if created_count % 1000 == 0:
            print(f"✓ تم إنشاء {created_count:,} إجابة... ({created_count/total_answers*100:.1f}%)")
    
    print(f"\n✓ تم إنشاء {created_count:,} إجابة بنجاح!")
    return created_count


